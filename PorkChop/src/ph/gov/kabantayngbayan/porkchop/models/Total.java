package ph.gov.kabantayngbayan.porkchop.models;

public class Total {

	private String year;
	private String gaa_total;
	private String new_appro_total;
	private String id;

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getGaa_total() {
		return gaa_total;
	}

	public void setGaa_total(String gaa_total) {
		this.gaa_total = gaa_total;
	}

	public String getNew_appro_total() {
		return new_appro_total;
	}

	public void setNew_appro_total(String new_appro_total) {
		this.new_appro_total = new_appro_total;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

}
